package com.zufe.bean;

public class LostTeam {
	private int lostId;
	private int teamId;
	private int id;

	public int getLostId() {
		return lostId;
	}

	public void setLostId(int lostId) {
		this.lostId = lostId;
	}

	public int getTeamId() {
		return teamId;
	}

	public void setTeamId(int teamId) {
		this.teamId = teamId;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
